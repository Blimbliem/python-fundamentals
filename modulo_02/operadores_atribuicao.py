# atribuição simples
saldo = 500

#atribuição + adição
saldo += 200

#atribuição + subtração
saldo -=40

print(saldo)

#atribuição + divisão
saldo /= 5
print(saldo)

#atribuição + divisão inteira
saldo //= 5
print(saldo)

#atribuição + multiplicação
saldo *= 3
print(saldo)

#atribuição + módulo
saldo %= 4
print(saldo)

#atribuição + potência
saldo **= 2
print(saldo)
